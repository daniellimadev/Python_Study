# Data type bool (boolean)
# When questioning something in a program,
# there are only two possible answers:
# yes (True) or no (False).
# There are several operators for "questioning".
# Among them ==, which is a logical operator that
# asks if one value is equal to another
print(10 == 10)  # Yes => True (true)
print(10 == 11)  # No => False (False)
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 11))