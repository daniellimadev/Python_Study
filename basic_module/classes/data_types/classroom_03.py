# int and float types
# int -> Integer
# The int type represents any number
# positive or negative. unsigned int is considered
# positive.
# print(11) # int
# print(-11) # int
# print(0)

# float -> Floating point number
# The float type represents any number
# positive or negative with floating point.
# unsigned float is considered positive.
# print(1.1, 10.11)
# print(0.0, -1.5)

# The type function shows the type that Python
# inferred to the value.
print(type('Daniel'))
print(type(0))
print(type(1.1), type(-1.1), type(0.0))